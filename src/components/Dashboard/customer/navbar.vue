<script setup>
import { computed, ref, watch } from "vue";
import { useRoute, useRouter } from "vue-router";

const props = defineProps({
  open: { type: Boolean, default: false },
  modelValue: { type: String, default: "" },
});

const emit = defineEmits(["close", "update:modelValue", "navigate"]);

const route = useRoute();
const router = useRouter();







const reportPagesOpen = ref(false);









const routeMap = {
  dashboard: "/dashboard",
  profile: "/profile",
  reference: "/reference",
  tree: "/tree-user",
  order: "/orders-list",
  order_details: "/orders-details",
  tracking: "/orders-tracking",
  account: "/account",
  withdraw: "/withdraw",

  sales_reports: "/reports-sales",
  withdrawal_reports: "/reports-withdrawal",

  customer_Notice: "/notice",
  marketing_strategy: "/marketing-strategy",
  
  setting: "/setting",
  logout: "/login",
};

const routeMatch = [
  { key: "dashboard", prefixes: ["/dashboard"] },
  { key: "profile", prefixes: ["/profile"] },
  { key: "reference", prefixes: ["/reference"] },
  { key: "tree", prefixes: ["/tree-user"] },
  { key: "order", prefixes: ["/orders-list"] },
  { key: "order_details", prefixes: ["/orders-details"] },
  { key: "tracking", prefixes: ["/orders-tracking"] },
  { key: "account", prefixes: ["/account"] },
  { key: "withdraw", prefixes: ["/withdraw"] },

  { key: "sales_reports", prefixes: ["/reports-sales"] },
  { key: "withdrawal_reports", prefixes: ["/reports-withdrawal"] },

  { key: "customer_Notice", prefixes: ["/notice"] },
  { key: "marketing_strategy", prefixes: ["/marketing-strategy"] },

  { key: "setting", prefixes: ["/setting"] },
  { key: "logout", prefixes: ["/logout"] },
];








const activeKey = computed(() => {
  const path = route.path;

  if (path.startsWith("/orders-details")) return "order";
  if (path.startsWith("/orders-list")) return "order";
  if (path.startsWith("/notice")) return "customer_Notice";
  if (path.startsWith("/marketing-strategy")) return "marketing_strategy";

  const hit = routeMatch.find((r) =>
    r.prefixes.some((pre) => path.startsWith(pre))
  );

  return hit?.key ?? "dashboard";
});









// report page open/close
watch(
  () => activeKey.value,
  (k) => {
    const reportKeys = ["sales_reports", "withdrawal_reports"];

    reportPagesOpen.value = reportKeys.includes(k);
  },
  { immediate: true }
);









async function pick(key) {
  emit("update:modelValue", key);
  emit("navigate", key);

  if (key === "logout") {
    try {
      await api.post("/auth/logout");
    } catch (e) {
      // ignore
    } finally {
      localStorage.removeItem("token");
      emit("close");
      return router.push("/login");
    }
  }

  const to = routeMap[key];
  if (to && route.path !== to) {
    await router.push(to);
  }

  emit("close");
}

